package btLyThuyet.encapsulation;

public class Encapsulation {
    private int a, b, c;
// getter là để xuất dữ liệu
    public int getA() {
        return a;
    }
// setter là để thiết lập dữ liệu
    public void setA(int a) {
        this.a = a;
    }
    public int getB(){
        return b;
    }
    public void setB(int b){
        this.b = b;
    }
    public int getC(){
        return c;
    }
    public void setC(int c){
        this.c=c;
    }
}
